"use strict";
//Places
let place = ['Sweden', 'Finland', 'England', 'Newzeland', 'Switzerland'];
// Original form
console.log('\n orignal \n ' + place);
// Alphabetical order
console.log('\n alphabetical \n ' + [...place].sort());
// Original form
console.log('\n orignal \n ' + place);
// Reverse alphabatical
console.log('\n reversed \n ' + [...place].sort().reverse());
// Original form
console.log('\n orignal \n ' + place);
// Reverse list
console.log('\n reversed \n ' + place.reverse());
// Orignal form while reversing
console.log('\n reversed \n ' + place.reverse());
// Alphabetical order
console.log('\n alphabetical \n ' + place.sort());
// Alphabetical order reversed
console.log('\n alphabetical \n ' + place.reverse());
